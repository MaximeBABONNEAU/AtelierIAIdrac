  function drawGrid(ctx, grid, colors, scale, ox, oy) {
    for (var y = 0; y < SIZE; y++) {
      var row = grid[y]; if (!row) continue;
      for (var x = 0; x < SIZE; x++) {
        var ch = row[x];
        if (ch && ch !== '.' && colors[ch]) {
          ctx.fillStyle = colors[ch];
          ctx.fillRect(ox + x * scale, oy + y * scale, scale, scale);
        }
      }
    }
  }

  // Dessine une créature. creature = objet dex. evoStage 0/1/2. equipped = [cosmeticId].
  function drawCreature(ctx, creature, evoStage, equipped, scale, ox, oy) {
    if (!creature) return;
    scale = scale || 8; ox = ox || 0; oy = oy || 0; evoStage = evoStage || 0;
    equipped = Array.isArray(equipped) ? equipped : [];
    var P = creature.palette || { p: '#888', s: '#ccc', a: '#fd0', o: '#222' };
    var r = creature.recipe || { body: 0, ears: 0, eyes: 0, belly: 0, tail: 0 };
    var colors = {
      O: P.o, B: P.p, D: darken(P.p, 0.72), S: P.s, A: P.a,
      E: '#ffffff', P: '#171430', M: darken(P.p, 0.45),
      W: 'rgba(255,255,255,0.45)',
      H: darken(P.a, 0.85), C: P.a, G: '#f5b731', Y: '#e74c3c', K: '#171430', R: '#e74c3c'
    };

    // Couches arrière -> avant
    if (evoStage >= 2) drawGrid(ctx, EVO2_WINGS, colors, scale, ox, oy);
    drawGrid(ctx, TAILS[r.tail] || TAILS[0], colors, scale, ox, oy);
    drawGrid(ctx, BODIES[r.body] || BODIES[0], colors, scale, ox, oy);
    drawGrid(ctx, BELLIES[r.belly] || BELLIES[0], colors, scale, ox, oy);
    drawGrid(ctx, EARS[r.ears] || EARS[0], colors, scale, ox, oy);
    drawGrid(ctx, EYES[r.eyes] || EYES[0], colors, scale, ox, oy);
    if (evoStage >= 1) drawGrid(ctx, EVO1, colors, scale, ox, oy);
    if (evoStage >= 2) { drawGrid(ctx, EVO2_HORNS, colors, scale, ox, oy); drawGrid(ctx, EVO2_GEM, colors, scale, ox, oy); }
    // Cosmétiques (au-dessus)
    equipped.forEach(function (id) { if (COSMETICS[id]) drawGrid(ctx, COSMETICS[id], colors, scale, ox, oy); });
  }

  // Rendu pratique dans un <canvas> donné, avec fond stage en dégradé radial type-coloré.
  function paintToCanvas(canvas, creature, evoStage, equipped, opts) {
    if (!canvas || !canvas.getContext) return;
    opts = opts || {};
    var ctx = canvas.getContext('2d');
    var scale = Math.floor((canvas.width) / SIZE);
    var ox = Math.floor((canvas.width - scale * SIZE) / 2);
    var oy = Math.floor((canvas.height - scale * SIZE) / 2);
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    ctx.imageSmoothingEnabled = false;
    if (opts.stage && creature) {
      var g = ctx.createRadialGradient(canvas.width / 2, canvas.height / 2, scale, canvas.width / 2, canvas.height / 2, canvas.width / 1.4);
      g.addColorStop(0, hexA(creature.palette.a, 0.20));
      g.addColorStop(1, 'rgba(0,0,0,0)');
      ctx.fillStyle = g; ctx.fillRect(0, 0, canvas.width, canvas.height);
    }
    drawCreature(ctx, creature, evoStage, equipped, scale, ox, oy);
  }

  function hexA(hex, a) {
    var h = (hex || '#fff').replace('#', '');
    if (h.length === 3) h = h[0] + h[0] + h[1] + h[1] + h[2] + h[2];
    return 'rgba(' + parseInt(h.slice(0, 2), 16) + ',' + parseInt(h.slice(2, 4), 16) + ',' + parseInt(h.slice(4, 6), 16) + ',' + a + ')';
  }

  window.AIA = window.AIA || {};
