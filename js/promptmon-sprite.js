  window.AIA.PROMPTMON.paintToCanvas = paintToCanvas;
  window.AIA.PROMPTMON.COSMETICS = COSMETICS;
  window.AIA.PROMPTMON.COSMETIC_SHOP = COSMETIC_SHOP;
